# Integers #
IO.puts(255)
IO.puts(0b11111111) # 255 again, only in binary
IO.puts(0xff) # 255 again, only in hexadecimal

# Floats #
IO.puts(3.14)
IO.puts(1.0e-10)

# Booleans #
IO.puts(true)
IO.puts(false)

# Atoms #
IO.puts(:true)

# Strings #
IO.puts("Strings are UTF-8 encoded")
IO.puts("Please keep strings between double quotes")
IO.puts("Elixir strings support\nLine breaks")